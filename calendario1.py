numero = int(input('digite um numero de 1 a 7 '))

if numero == 1 :
    print('numero 1 é segunda')

elif numero == 2:
    print('numero 2 é terça')

elif numero == 3:
    print('numero 3 é quarta')

elif numero == 4:
    print('numero 4 é quinta')

elif numero == 5:
    print('numero 5 é sexta')

elif numero == 6:
    print('numero 6 é sabado')

elif numero == 7:
    print('numero 7 é domingo')

else :
    print('numero incorreto')